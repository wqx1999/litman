def _write_config(vault: Path, projects: dict[str, str]) -> None:
    cfg = vault / "lit-config.yaml"
    lines = [
        f"library_name: {vault.name}",
        "projects:",
    ]
    for name, path in projects.items():
        lines.append(f"  {name}: {path}")
    cfg.write_text("\n".join(lines) + "\n", encoding="utf-8")


def test_cli_refresh_views_writes_project_refs(vault: Path, tmp_path: Path) -> None:
    pf_dir = tmp_path / "pepforge_proj"
    pf_dir.mkdir()
    _write_config(vault, {"pepforge": str(pf_dir)})
    _make_paper(vault, "p1", projects=["pepforge"], priority="A")

    runner = CliRunner()
    result = runner.invoke(cli, ["refresh-views", "--library", str(vault)])
    assert result.exit_code == 0, result.output
    assert "Project REFERENCES.md" in result.output
    assert "pepforge: 1 paper" in result.output
    target = pf_dir / "litman_reflib" / "REFERENCES.md"
    assert target.is_file()
    body = target.read_text(encoding="utf-8")
    assert "p1" in body
    assert "Priority A" in body


def test_cli_refresh_views_no_projects_no_section(vault: Path) -> None:
    """When config has empty `projects: {}` the project block is silent."""
    runner = CliRunner()
    result = runner.invoke(cli, ["refresh-views", "--library", str(vault)])
    assert result.exit_code == 0, result.output
    assert "Project REFERENCES.md" not in result.output



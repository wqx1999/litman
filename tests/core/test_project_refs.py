def test_write_creates_literature_subdir(vault: Path, tmp_path: Path) -> None:
    project_dir = tmp_path / "pepforge_proj"
    project_dir.mkdir()
    _make_paper(vault, "p1", projects=["pepforge"])
    target = write_references_md(vault, "pepforge", project_dir)
    assert target == project_dir / LITERATURE_SUBDIR / REFERENCES_FILENAME
    assert target.is_file()
    assert (project_dir / LITERATURE_SUBDIR).is_dir()


def test_write_idempotent(vault: Path, tmp_path: Path) -> None:
    project_dir = tmp_path / "pepforge_proj"
    project_dir.mkdir()
    _make_paper(vault, "p1", projects=["pepforge"])
    first = write_references_md(vault, "pepforge", project_dir, now="2026-05-11T01:00:00+02:00")
    second = write_references_md(vault, "pepforge", project_dir, now="2026-05-11T02:00:00+02:00")
    assert first == second
    # Body differs only in the timestamp banner — content is otherwise stable.
    assert second.read_text(encoding="utf-8").count("Priority B") == 1



def test_rebuild_all_writes_each_registered_project(vault: Path, tmp_path: Path) -> None:
    pf_dir = tmp_path / "pepforge_proj"
    pf_dir.mkdir()
    pc_dir = tmp_path / "pepcodec_proj"
    pc_dir.mkdir()

    _make_paper(vault, "p1", projects=["pepforge"])
    _make_paper(vault, "p2", projects=["pepcodec"])
    _make_paper(vault, "p3", projects=["pepforge", "pepcodec"])  # shared

    results = rebuild_all_project_refs(
        vault, {"pepforge": str(pf_dir), "pepcodec": str(pc_dir)}
    )
    assert set(results.keys()) == {"pepforge", "pepcodec"}
    assert results["pepforge"]["status"] == "written"
    assert results["pepcodec"]["status"] == "written"
    assert results["pepforge"]["n_papers"] == 2
    assert results["pepcodec"]["n_papers"] == 2
    assert (pf_dir / "litman_reflib" / "REFERENCES.md").is_file()
    assert (pc_dir / "litman_reflib" / "REFERENCES.md").is_file()


def test_rebuild_all_skips_missing_project_dir(vault: Path, tmp_path: Path) -> None:
    """Project dir absent on this machine -> skip, don't abort the loop."""
    pf_dir = tmp_path / "pepforge_proj"
    pf_dir.mkdir()
    _make_paper(vault, "p1", projects=["pepforge"])
    _make_paper(vault, "p2", projects=["nowhere"])

    results = rebuild_all_project_refs(
        vault,
        {
            "pepforge": str(pf_dir),
            "nowhere": str(tmp_path / "does_not_exist"),
        },
    )
    assert results["pepforge"]["status"] == "written"
    assert results["nowhere"]["status"] == "skipped"
    assert "not found" in results["nowhere"]["detail"]
    # Still counts paper tagged with the missing project so user knows.
    assert results["nowhere"]["n_papers"] == 1



def test_render_authors_truncation() -> None:
    """1 author -> just the family; 2 -> both families; 3+ -> first two + 'et al.'"""
    from litman.core.project_refs import _format_authors
    assert _format_authors(["Doe, J."]) == "Doe"
    assert _format_authors(["Doe, J.", "Roe, K."]) == "Doe, Roe"
    assert _format_authors(["A, x", "B, y", "C, z", "D, w"]) == "A, B et al."



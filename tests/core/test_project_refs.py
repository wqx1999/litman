def test_render_banner_present(vault: Path) -> None:
    out = render_references_md(vault, "pepforge", now="2026-05-11T15:00:00+02:00")
    assert out.startswith(
        "<!-- AUTO-GENERATED by `lit refresh-views` — DO NOT EDIT -->"
    )
    assert "Last updated: 2026-05-11T15:00:00+02:00" in out


def test_render_empty_project_message(vault: Path) -> None:
    """No papers tagged with project -> friendly message, no priority sections."""
    _make_paper(vault, "2024_Doe_Other", projects=["different"])
    out = render_references_md(vault, "pepforge")
    assert "No papers tagged" in out
    assert "lit modify <id> --add-tag projects=pepforge" in out
    assert "## Priority" not in out


def test_render_groups_by_priority(vault: Path) -> None:
    _make_paper(vault, "2024_A_paper", projects=["pepforge"], priority="A")
    _make_paper(vault, "2023_B_paper", projects=["pepforge"], priority="B")
    _make_paper(vault, "2022_C_paper", projects=["pepforge"], priority="C")
    out = render_references_md(vault, "pepforge")
    # A appears before B which appears before C.
    a_pos = out.index("Priority A")
    b_pos = out.index("Priority B")
    c_pos = out.index("Priority C")
    assert a_pos < b_pos < c_pos


def test_render_unprioritized_bucket_last(vault: Path) -> None:
    _make_paper(vault, "2024_A_paper", projects=["pepforge"], priority="A")
    _make_paper(vault, "2023_X_paper", projects=["pepforge"], priority=None)
    out = render_references_md(vault, "pepforge")
    a_pos = out.index("Priority A")
    u_pos = out.index("Unprioritized")
    assert a_pos < u_pos


def test_render_year_desc_within_group(vault: Path) -> None:
    _make_paper(vault, "2020_Older_paper", projects=["pepforge"], priority="A", year=2020)
    _make_paper(vault, "2024_Newer_paper", projects=["pepforge"], priority="A", year=2024)
